package org.narrowingWidening;

public class CharToInt {

	public static void main(String[] args) {
		char c='C';
		int i=c;
		System.out.println("Char = "+c);
		System.out.println("Int = "+i);
		float f=c;
		double d=c;
		System.out.println("Float = "+f);
		System.out.println("Double = "+d);
	}

}
