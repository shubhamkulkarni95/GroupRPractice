package org.basicOperator;

public class BasicOperator {

	public static void main(String[] args) {
		int a=10,b=20;
		int sum=a+b;
		System.out.println("Sum : "+sum);
		System.out.println("Sum : "+a+b);
		System.out.println("Sum : "+(a+b));
		System.out.println((a+b)+" : Sum");
		System.out.println(a+b+" : Sum");
	}

}
