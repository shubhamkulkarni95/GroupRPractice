package org.array;

public class OneDArrayEx2 {

	public static void main(String[] args) {
		float ar[]=new float[4];
		ar[0]=10.26f;
		ar[1]=12.6f;
		ar[2]=10.65f;
		ar[3]=1.65f;
		System.out.println("Array lenght : "+ar.length);
		for(int i=0;i<ar.length;i++) {
			System.out.println("ar["+i+"] = "+ar[i]);
		}
		
		
	}

}
